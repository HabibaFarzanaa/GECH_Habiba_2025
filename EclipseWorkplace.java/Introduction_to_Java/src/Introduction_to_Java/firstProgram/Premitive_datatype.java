package Introduction_to_Java.firstProgram;

public class Premitive_datatype {
	public static void main(String[] args) {
	/*
	 * DATATYPES IN JAVA
	 * 1.byte = -127 - +127
	 * 2.short = 300
	 * 3.Int = 3000
	 * 4.long = 300L
	 * 5.float = 3.1f
	 * 6.double = 9.1
	 * 7.char = 'a'
	 * 8.boolean = true/false
	 */
	
	/*
	 * DECLARATION VARIABLES
	 * Variable: variable is a container used to store a value based on data type
	*/
	
	byte age;
	age = 20;
	
	byte age1 = 30;
	short shrt = 300;
	int Int = 3000;
	long lng = 300L;
	float flt = 3.1f;
	double dbl = 9.1;
	char chr = 'a';
	boolean bln = true;
	System.out.println(age);
	System.out.println(age1);
	System.out.println(shrt);
	System.out.println(Int);
	System.out.println(lng);
	System.out.println(flt);
	System.out.println(dbl);
	System.out.println(chr);
	System.out.println(bln);
	}	
}
