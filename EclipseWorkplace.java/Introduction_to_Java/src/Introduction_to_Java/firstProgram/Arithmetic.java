package Introduction_to_Java.firstProgram;
import java.io.*;
public class Arithmetic {

	public static void main(String[] args) {
		
		// Arithmetic operators on integers
		        int a = 10;
		        int b = 3;

		        System.out.println("a + b = " + (a + b));
		        System.out.println("a - b = " + (a - b));
		        System.out.println("a * b = " + (a * b));
		        System.out.println("a / b = " + (a / b));
		        System.out.println("a % b = " + (a % b));
		          
		    }
		
	}


